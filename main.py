import turtle;

def turtleInit():
    s = turtle.getscreen()
    t = turtle.Turtle()
    t.forward(100)
    t.right(90)
    turtle.Screen().exitonclick();


# Press the green button in the gutter to run the script.
if __name__ == '__main__':
    turtleInit()
